//Defines two integers which go through mathematical operations, then prints out their values

public class Numbers
{

    public static void main(String [] args)
    {

      int max;
      int min = 10;
      max = (17 - 4 / 10);
      max = max + 6;
      min = max - min;
      System.out.println(max * 2);
      System.out.println(max + min);
      System.out.println(max);
      System.out.println(min);
    }
}

//Output from program:

/*
46
36
23
13
*/

// Note: Numbers round to nearest integer prior to calculating